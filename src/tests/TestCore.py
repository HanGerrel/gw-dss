import unittest

from core.core import SMO


class TestCore(unittest.TestCase):
    def test_solve(self):
        solution = [
            [1.999999999999988, 19.75000000000001],
            [2.9999999865419777, 19.333333364471503],
            [3.9999544877323903, 18.50022042445289],
            [4.988404217896938, 16.138763477608368],
            [5.638015516537364, 8.932217231523072],
            [5.470058657877014, 2.6392496943902386],
            [5.230769347078653, 0.8040942209640478],
            [5.104507880490093, 0.27858391070099],
            [5.044726230512489, 0.10063015923423867],
            [5.0180525765917, 0.03610498101962622]
        ]
        for i in range(0, 10):
            self.smo = SMO(i + 1, 20, 60, 12)
            self.assertEqual(self.smo.solve(), solution[i])


if __name__ == '__main__':
    unittest.main()
